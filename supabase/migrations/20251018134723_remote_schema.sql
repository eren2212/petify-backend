CREATE OR REPLACE FUNCTION public.auto_hide_reported_review()
 RETURNS trigger
 LANGUAGE plpgsql
AS $function$
DECLARE
    report_count INTEGER;
BEGIN
    -- Bu yoruma ka rapor var?
    SELECT COUNT(*) INTO report_count
    FROM review_reports 
    WHERE review_id = NEW.review_id;
    
    -- 5 veya daha fazla rapor varsa yorumu otomatik gizle
    IF report_count >= 5 THEN
        UPDATE reviews 
        SET status = 'rejected',
            rejection_reason = 'Otomatik gizlendi: 5+ rapor alnd',
            reviewed_at = NOW()
        WHERE id = NEW.review_id;
    END IF;
    
    RETURN NEW;
END;
$function$
;

